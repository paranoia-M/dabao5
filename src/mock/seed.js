// 各 storageKey 对应的 store 内 stateName（与 stores 中的 ref 名严格一致）
const STATE_NAME_MAP = {
  "sheji-sample" : "sampleList",
  "sheji-detectTask" : "detectTaskList",
  "sheji-inspectionResult" : "inspectionResultList",
  "sheji-inspectionReport" : "inspectionReportList",
  "sheji-detectStandard" : "detectStandardList",
  "sheji-alertConfig" : "alertConfigList",
  "sheji-alertRecord" : "alertRecordList"
}

export function seedIfEmpty() {
  for (const [key, value] of Object.entries(SEED)) {
    if (!localStorage.getItem(key)) {
      const stateName = STATE_NAME_MAP[key]
      if (!stateName) continue
      // pinia-plugin-persistedstate 默认整个 state 序列化为对象
      localStorage.setItem(key, JSON.stringify({ [stateName]: value }))
    }
  }
}
