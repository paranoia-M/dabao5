// 各 storageKey 对应的 store 内 stateName（与 stores 中的 ref 名严格一致）
const STATE_NAME_MAP = {
  "sheji-inmate" : "inmateList",
  "sheji-inmateMovement" : "movementList",
  "sheji-patrolTask" : "patrolTaskList",
  "sheji-patrolRecord" : "patrolRecordList",
  "sheji-device" : "deviceList",
  "sheji-maintenanceRecord" : "maintenanceList",
  "sheji-alertEvent" : "alertEventList",
  "sheji-uploadRecord" : "uploadList",
  "sheji-patrolApproval" : "patrolApprovalList",
  "sheji-maintenanceApproval" : "maintenanceApprovalList"
}

export function seedIfEmpty() {
  for (const [key, value] of Object.entries(SEED)) {
    if (!localStorage.getItem(key)) {
      const stateName = STATE_NAME_MAP[key]
      if (!stateName) continue
      // pinia-plugin-persistedstate 默认整个 state 序列化为对象
      localStorage.setItem(key, JSON.stringify({ [stateName]: value }))
    }
  }
}
